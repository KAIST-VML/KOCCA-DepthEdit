import torch
import torch.nn as nn
from models.networks import sync_batchnorm
BatchNorm2d = sync_batchnorm.SynchronizedBatchNorm2d

def conv3x3_bn_relu(in_planes, out_planes, stride=1):
    "3x3 convolution + BN + relu"
    return nn.Sequential(
            nn.Conv2d(in_planes, out_planes, kernel_size=3,
                      stride=stride, padding=1, bias=False),
            BatchNorm2d(out_planes),
            nn.ReLU(inplace=True),
            )


class Attention(nn.Module):
    def __init__(self, in_channels):
        super(Attention, self).__init__()
        inter_channels = in_channels
        norm_layer = nn.BatchNorm2d

        self.conv_a1 = nn.Sequential(nn.Conv2d(in_channels, inter_channels, 3, padding=1, bias=False),
                                   norm_layer(inter_channels),
                                   nn.ReLU())
        
        self.conv_c1 = nn.Sequential(nn.Conv2d(in_channels, inter_channels, 3, padding=1, bias=False),
                                   norm_layer(inter_channels),
                                   nn.ReLU())

        self.sa = PAM_Module(inter_channels)
        self.sc = CAM_Module(inter_channels)

        self.conv_a2 = nn.Sequential(nn.Conv2d(inter_channels, inter_channels, 3, padding=1, bias=False),
                                   norm_layer(inter_channels),
                                   nn.ReLU())
        self.conv_c2 = nn.Sequential(nn.Conv2d(inter_channels, inter_channels, 3, padding=1, bias=False),
                                   norm_layer(inter_channels),
                                   nn.ReLU())

    def forward(self, x):
        x_a = self.conv_a2(self.sa(self.conv_a1(x)))
        x_c = self.conv_c2(self.sc(self.conv_c1(x)))

        return x_a + x_c

